<?php
//deklarasi
$x = "adik"; //string
$y = "kakak"; //string
$z = 99; //integer

// equal ( sama dengan )
$x == $y; // adik sama dengan kakak

// indetical ( persis )
$x === $y; // adik sama dengan kakak termasuk tipe datanya

// not equal ( tidak sama dengan )
$x != $z; // adik tidak sama dengan 99
$x <> $z; // adik tidak sama dengan 99

// not indentical ( tidak persis )
$x !== $z; // adik tidak persis dengan 99

// Greater ( Lebih Dari )
$x > $y; // adik lebih baik dari kka

// Greater Equal ( Lebih dari sama dengan )
$x >= $y;

// less ( Kurang dari )
$x < $y; // adik kurang baik dari kka

// Less Equal ( Kurang dari sama dengan )
$x <= $y;
