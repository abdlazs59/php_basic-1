<?php

//deklarasi 
$x = "abu bakar azka adalah laki laki"; //True
$y = "rafa maritza adalah perempuan"; //True
$z = "Pak yadi guru bahasa inggris"; //False

//gerbang logika AND
$x and $y; //output bernilai TRUE jika keduanya adalah TRUE
$x && $y;

//gerbang logika OR
$x or $y; //output bernilai TRUE jika keduanya adalah TRUE
$x || $y;

//gerbang Logika Xor
$x xor $z; //output bernilai TRUE jika salah satunya adalah true
$y xor $z;

//gerbang logika not
!$x; // bernilai false
!$y; // bernilai false
